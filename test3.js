//JS Local storage API practice

function setSession(){
    localStorage.setItem("name","Rashed");
}

function getSession(){
    localStorage.setItem("age","32");
}

function removeItem(){
    localStorage.removeItem("name");
}

function cleareLocalStorage(){
    localStorage.clear();
}

function getSessionKey(){
    localStorage.key(0);
}

function getTotalSession(){
    localStorage.length;
}

